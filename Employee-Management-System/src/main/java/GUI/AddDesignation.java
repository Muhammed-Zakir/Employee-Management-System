    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(txtDesignationID.getText().isEmpty()||txtDesignationName.getText().isEmpty()||
          txtAcDepartment.getText().isEmpty()||txtYrsExperience.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Fileds cannot be empty, When adding a new Designation." ,
                "Add a new Designation", JOptionPane.WARNING_MESSAGE);
        }
        else{

            Designation.setDesignationId(txtDesignationID.getText());
            Designation.setDesignationName(txtDesignationName.getText());
            Designation.setAssociatedDepartment(txtAcDepartment.getText());
            Designation.setYrsOfExperience(Integer.parseInt(txtYrsExperience.getText()));  
        }
        if(Designation.addNewDesignation()){
            JOptionPane.showMessageDialog(null,"Designation Added successfully" ,"Add a new Designation",
                    JOptionPane.INFORMATION_MESSAGE);
        }   else {
                JOptionPane.showMessageDialog(null,"Sorry! Something went wrong with the application, "
                    + "Will be fixing it soon." ,"Add a new Designation", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_btnSaveActionPerformed

